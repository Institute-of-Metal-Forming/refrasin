public static class Constants
{
    /// <summary>
    /// Pi. What else?
    /// </summary>
    public const double Pi = Math.PI;

    /// <summary>
    /// A half of Pi.
    /// </summary>
    public const double HalfOfPi = Pi / 2;

    /// <summary>
    /// A third of Pi.
    /// </summary>
    public const double ThirdOfPi = Pi / 3;

    /// <summary>
    /// A quarter of Pi.
    /// </summary>
    public const double QuarterOfPi = Pi / 4;

    /// <summary>
    /// A sixth of Pi.
    /// </summary>
    public const double SixthOfPi = Pi / 6;

    /// <summary>
    /// A eighth of Pi.
    /// </summary>
    public const double EighthOfPi = Pi / 8;

    /// <summary>
    /// Two times Pi.
    /// </summary>
    public const double TwoPi = Pi * 2;

